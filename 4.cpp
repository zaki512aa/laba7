#include <iostream>
using namespace std;

double* getMaxPointer(double *a, double *b, double *c) {

    if (*a >= *b && *a >= *c) {
        return a;
    } else if (*b >= *a && *b >= *c) {
        return b;
    } else {
        return c;
    }
}

int main() {
    double x = 10.0;
    double y = 90.0;
    double z = 20.0;

    cout << "do izmeneniya: " << x << ", " << y << ", " << z << endl;

    // 1. Вычисляем среднее значение
    double average = (x + y + z) / 3.0;
    cout << "srednee znachenie: " << average << endl;

    *getMaxPointer(&x, &y, &z) = average;

    cout << "Posle zameny max na srednee: " << endl;
    cout << "x: " << x << endl;
    cout << "y: " << y << " srednee" << endl;
    cout << "z: " << z << endl;

    return 0;
}
